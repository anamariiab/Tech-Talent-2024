
public class Estudiante {
	
	protected String nombre;
	protected int edad;
	protected String sexo;
	protected int calificacion;
	
	public Estudiante(String nombre, int edad, String sexo, int calificacion) {
		this.nombre = nombre;
		this.edad = edad;
		this.sexo = sexo;
		this.calificacion = calificacion;
	}
	
	

}
